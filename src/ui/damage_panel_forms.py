def canonical_display_name(name_ja: str) -> str:
    return FORM_CANONICAL_NAME.get(name_ja or "", name_ja or "---")


def normalize_form_name(name_ja: str, form_name_to_group: dict[str, list[str]]) -> str:
    """Return canonical form key for name_ja, resolving spacing variants."""
    if name_ja in form_name_to_group:
        return name_ja
    spaced = re.sub(r"(?<!\s)\(", " (", name_ja or "")
    if spaced != name_ja and spaced in form_name_to_group:
        return spaced
    return name_ja


def form_group(name_ja: str, form_name_to_group: dict[str, list[str]]) -> list[str]:
    key = normalize_form_name(name_ja, form_name_to_group)
    return form_name_to_group.get(key, [])


def next_form_name(name_ja: str, form_name_to_group: dict[str, list[str]]) -> str | None:
    key = normalize_form_name(name_ja, form_name_to_group)
    group = form_name_to_group.get(key)
    if not group or len(group) < 2:
        return None
    idx = group.index(key) if key in group else 0
    return group[(idx + 1) % len(group)]
